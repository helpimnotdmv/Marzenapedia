function saveArticle() {
  const slug = document.getElementById('editor-filename').value.trim().toLowerCase().replace(/\s+/g, '-');
  const content = document.getElementById('editor-content').value;
  if (!slug) { alert('Please enter a slug for the article.'); return; }
  if (!content.trim()) { alert('Article content is empty.'); return; }

  const filename = `${ARTICLES_PATH}/${slug}.md`;
  const encoded = encodeURIComponent(content);
  const githubUrl = `https://github.com/${REPO_OWNER}/${REPO_NAME}/new/${BRANCH}?filename=${encodeURIComponent(filename)}&value=${encoded}`;

  alert(`Opening GitHub to save this article.\n\nFile: ${filename}\n\nIf the file already exists, GitHub will let you edit the existing version. Once you commit, the index will rebuild automatically.`);
  window.open(githubUrl, '_blank');

  State.articleCache[slug] = content;
  State.index = null; // invalidate
  closeEditor();
}

