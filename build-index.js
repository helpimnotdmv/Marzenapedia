/* ═══════════════════════════════════════════════════════════════
   MARZENAPEDIA — Application JavaScript (Phase 1)
   ═══════════════════════════════════════════════════════════════
   Architecture:
   - Hash-based routing (#/article-slug, #/all, #/search/query)
   - index.json drives all article listing/search (auto-generated by GitHub Action)
   - Block tokenizer parser (extensible for Phase 3 :::blocks)
   - Article content fetched on-demand from /articles/{slug}.md
   - Last-edited dates from GitHub commits API (cached)
   - Dark mode via [data-theme] attribute on <html>
   - In-universe date system: ISO dates → CET display, used by timeline
   ═══════════════════════════════════════════════════════════════ */

