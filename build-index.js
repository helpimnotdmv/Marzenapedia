// CET CLOCK (in-universe time display)
// ═══════════════════════════════════════════════════════════════
function startCetClock() {
  const tick = () => {
    const now = new Date();
    // Force CET (Europe/Paris) display regardless of user's timezone
    const opts = {
      timeZone: 'Europe/Paris',
      hour: '2-digit',
      minute: '2-digit',
      second: '2-digit',
      hour12: false
    };
    const time = now.toLocaleTimeString('en-GB', opts);
    const el = document.getElementById('cet-time');
    if (el) el.textContent = time;
  };
  tick();
  setInterval(tick, 1000);
}

// In-universe date formatter — used by timeline blocks (Phase 3)
// Takes an ISO date string, returns "2 May 1952" formatted in CET context
function formatInUniverseDate(iso) {
  if (!iso) return '';
  const d = new Date(iso);
  if (isNaN(d.getTime())) return iso;
  return d.toLocaleDateString('en-GB', {
    timeZone: 'Europe/Paris',
    day: 'numeric',
    month: 'long',
    year: 'numeric'
  });
}

// ═══════════════════════════════════════════════════════════════
// HASH ROUTING
// Routes:  #/                  → home
//          #/all               → all articles
//          #/search/{q}        → search results
//          #/{slug}            → article
//          #/{slug}#{section}  → article with section anchor (handled separately)
// ═══════════════════════════════════════════════════════════════
function parseHash() {
  let hash = location.hash || '#/';
  if (!hash.startsWith('#/')) hash = '#/' + hash.replace(/^#/, '');
  const path = hash.slice(2); // strip "#/"

  if (!path || path === 'home') return { view: 'home' };
  if (path === 'all') return { view: 'all' };
  if (path.startsWith('search/')) {
    return { view: 'search', query: decodeURIComponent(path.slice(7)) };
  }
  // Article — may have a section anchor after a second "#"
  const [slug, section] = path.split('#');
  return { view: 'article', slug: decodeURIComponent(slug), section: section ? decodeURIComponent(section) : null };
}

function navigate(target, slugOrQuery) {
  if (target === 'home') {
    location.hash = '#/';
  } else if (target === 'all') {
    location.hash = '#/all';
  } else if (target === 'article' && slugOrQuery) {
    location.hash = '#/' + encodeURIComponent(slugOrQuery);
  } else if (target === 'search' && slugOrQuery) {
    location.hash = '#/search/' + encodeURIComponent(slugOrQuery);
  } else {
    location.hash = '#/';
  }
}

async function handleRoute() {
  const route = parseHash();
  State.view = route.view;
  State.slug = route.slug || null;

  document.getElementById('page-tabs').style.display = route.view === 'article' ? 'flex' : 'none';

  // Hide search dropdown
  const dd = document.getElementById('search-dropdown');
  if (dd) dd.classList.remove('visible');

  // Disconnect any existing scroll observer
  if (State.scrollObserver) { State.scrollObserver.disconnect(); State.scrollObserver = null; }

  if (route.view === 'home') await showHome();
  else if (route.view === 'all') await showAllArticles();
  else if (route.view === 'search') await showSearchResults(route.query);
  else if (route.view === 'article') await showArticle(route.slug, route.section);
}

window.addEventListener('hashchange', handleRoute);

