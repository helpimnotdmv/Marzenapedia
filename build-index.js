async function showArticle(slug, sectionAnchor) {
  document.getElementById('main-content').innerHTML = '<div class="loading">Loading article</div>';
  document.title = `${slugToTitle(slug)} — Marzenapedia`;

  await fetchIndex(); // ensure index is loaded for redlink detection

  let md;
  try {
    md = await fetchArticle(slug);
  } catch (e) {
    renderArticleNotFound(slug);
    return;
  }

  const fm = parseFrontmatter(md);
  const title = extractTitle(md) || slugToTitle(slug);
  document.title = `${title} — Marzenapedia`;

  // Strip the title line from body so we don't render it twice
  const body = extractFrontmatter(md).body.replace(/^# .+\n/, '');
  const bodyHtml = parseMarkdown(body);

  // Build TOC from h2 elements
  const tocHtml = buildToc(bodyHtml);

  // Last-edited date
  const lastEdited = await fetchLastEdited(slug);
  const editedStr = lastEdited
    ? new Date(lastEdited).toLocaleDateString('en-GB', { timeZone: 'Europe/Paris', day: 'numeric', month: 'long', year: 'numeric' })
    : null;

  // Article meta line (in-universe date, last edited, sources)
  const metaParts = [];
  if (fm.date) metaParts.push(`<span class="meta-item">${formatInUniverseDate(fm.date)}</span>`);
  if (editedStr) metaParts.push(`<span class="meta-item">Last revised ${editedStr}</span>`);
  if (fm.tags && fm.tags.length) metaParts.push(`<span class="meta-item">${fm.tags.join(' · ')}</span>`);
  const metaHtml = metaParts.length ? `<div class="article-meta">${metaParts.join('')}</div>` : '';

  // Named sources block (foundation for full named-source system in later phases)
  let sourcesHtml = '';
  if (fm.sources && fm.sources.length) {
    sourcesHtml = `<div style="margin-top:36px;padding:14px 18px;background:var(--paper-warm);border-left:3px solid var(--gold);font-family:'Crimson Pro',serif;font-size:14px;">
      <strong style="font-family:'Source Sans 3',sans-serif;font-size:11px;text-transform:uppercase;letter-spacing:0.08em;color:var(--navy);display:block;margin-bottom:6px;">Cited Sources</strong>
      <ul style="margin:0 0 0 18px;font-style:italic;">
        ${fm.sources.map(s => `<li>${escapeHtml(s)}</li>`).join('')}
      </ul>
    </div>`;
  }

  // Breadcrumb
  document.getElementById('breadcrumb').innerHTML =
    `<a onclick="navigate('home')">Main Page</a><span class="sep">·</span><a onclick="navigate('all')">All Articles</a><span class="sep">·</span>${escapeHtml(title)}`;

  document.getElementById('main-content').innerHTML = `
    <div class="layout">
      ${tocHtml}
      <article class="article clearfix">
        <div class="article-title">${escapeHtml(title)}</div>
        ${metaHtml}
        ${bodyHtml}
        ${sourcesHtml}
        <div style="clear:both;border-top:1px solid var(--border);margin-top:36px;padding-top:14px;font-size:11px;color:var(--muted-soft);font-style:italic;font-family:'Crimson Pro',serif;letter-spacing:0.02em;">
          Marzenapedia &nbsp;·&nbsp; Bureau of Records, Lévane &nbsp;·&nbsp; Fictional sovereign state
        </div>
      </article>
    </div>`;

  // Active TOC highlighting via IntersectionObserver
  setupScrollSpy();

  // Scroll to section anchor (#section after slug)
  if (sectionAnchor) {
    const target = document.getElementById(sectionAnchor);
    if (target) target.scrollIntoView({ behavior: 'smooth' });
  } else {
    window.scrollTo({ top: 0, behavior: 'auto' });
  }
}

